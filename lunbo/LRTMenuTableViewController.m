    return 1;
}


- (UITableViewCell *)tableView:(UITableView *)tableView cellForRowAtIndexPath:(NSIndexPath *)indexPath {
    UITableViewCell *cell = [tableView dequeueReusableCellWithIdentifier:@"cell"];
    if (!cell) {
        cell = [[UITableViewCell alloc] initWithStyle:UITableViewCellStyleValue1 reuseIdentifier:@"cell"];
    }
    cell.textLabel.text = [NSString stringWithFormat:@"第%ld步",indexPath.row + 1];
    return cell;
}

- (void)tableView:(UITableView *)tableView didSelectRowAtIndexPath:(NSIndexPath *)indexPath{
    LRTSecondCarouselLayout *layout = [[LRTSecondCarouselLayout alloc] init];
    layout.itemSize = CGSizeMake([UIScreen mainScreen].bounds.size.width/375.0 *130, [UIScreen mainScreen].bounds.size.width/375.0 *130);
    LRTCollectionViewController *vc = [[LRTCollectionViewController alloc] initWithCollectionViewLayout:layout];
    [self presentViewController:vc animated:YES completion:nil];
}
